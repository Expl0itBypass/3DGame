package com.gmail.lynx7478.game.engine;

public class Model {
	
	private int id;
	private int vertex;
	
	public Model(int id, int vertex)
	{
		this.id = id;
		this.vertex = vertex;
	}
	
	public int getID()
	{
		return id;
	}
	
	public int getVertex()
	{
		return vertex;
	}

}
